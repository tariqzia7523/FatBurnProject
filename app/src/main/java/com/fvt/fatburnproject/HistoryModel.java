package com.fvt.fatburnproject;

public class HistoryModel {
    String id;
    String name;
    String paymentId;
    String transectoionid;


    public HistoryModel() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPaymentId() {
        return paymentId;
    }

    public void setPaymentId(String paymentId) {
        this.paymentId = paymentId;
    }

    public String getTransectoionid() {
        return transectoionid;
    }

    public void setTransectoionid(String transectoionid) {
        this.transectoionid = transectoionid;
    }


}
